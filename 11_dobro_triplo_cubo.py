# 11. Faça um Programa que peça 2 números inteiros e um número real. Calcule e mostre:
    # a) o produto do dobro do primeiro com metade do segundo.
    # b) a soma do triplo do primeiro com o terceiro.
    # c) o terceiro elevado ao cubo.

num_1 = int(input('Digite um número interio: '))
num_2 = int(input('Digite outro número interio: '))
num_3 = float(input('Digite um número real: '))
print(f'a) resultado = {(num_1*2)*(num_2/2)}')
print(f'b) resultado = {(num_1*3) + num_3}')
print(f'c) resultado = {num_3**(3)}')
